<h2>Did you know...?</h2>

<p>Facts about irises:</p>

<ul>
	<li>Iris is the Greek word for rainbow.</li>
	<li>Iris is also the name of a Greek goddess.</li>
	<li>There are over 250 different species of irises.</li>
	<li>Irises grow from rhizomes or bulbs.</li>
	<li>Irises are perennial, meaning they come back every year.</li>
	<li>We often see irises in purple or blue, white, or yellow. They also can be found in:
		<ul>
			<li>pink</li>
			<li>peach</li>
			<li>orange</li>
			<li>burgundy</li>
			<li>black</li>
		</ul>
	</li>
	<li>The flowers have three sepals, which droop downward.</li>
	<li>The flowers also have three petals, which stand upright.</li>
	<li>Irises are found throughout the northern hemisphere.</li>
	<li>Many irises are reblooming and produce flowers from spring to fall.</li>
	
</ul>

<p>Sources: <a href="https://www.irises.org/" target="_blank">The American Iris Society</a> and <a href="https://en.wikipedia.org/wiki/Iris_(plant)" target="_blank">Wikipedia</a>.</p>

