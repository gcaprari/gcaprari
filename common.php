<h2>A Local Native</h2>

<img src="images/Iris_pseudacorus.jpg" alt="A yellow flag iris"
width="300" height="400">


<p>The yellow flag iris &#40;<span class="latin">Iris pseudacorus</span>&#41; is native to New Jersey and flourishes in sunny, wet areas, like the borders of ponds. Care must be taken when growing these, as they can quickly overtake other plants.</p>

<p>Sources: <a href="https://plants.ces.ncsu.edu/plants/iris-pseudacorus/" target="_blank">North Carolina Extension Gardener Plant Toolbox</a> and <a href="https://jersey-green.com/2020/07/09/native-and-invasive-irises-iridaceae/comment-page-1/" target="_blank">What's Growing in the Garden State</a>.</p>

